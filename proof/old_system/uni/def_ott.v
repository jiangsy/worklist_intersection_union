Inductive s_in : typvar -> typ -> Prop :=    (* defn s_in *)
 | s_in__var : forall (X:typvar),
     s_in X (typ_var_f X)
 | s_in__arrow1 : forall (X:typvar) (A1 A2:typ),
     lc_typ A2 ->
     s_in X A1 ->
     s_in X (typ_arrow A1 A2)
 | s_in__arrow2 : forall (X:typvar) (A1 A2:typ),
     lc_typ A1 ->
     s_in X A2 ->
     s_in X (typ_arrow A1 A2)
