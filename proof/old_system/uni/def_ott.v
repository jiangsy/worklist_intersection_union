     (lc_typ A1) ->
     (lc_typ A2) ->
     (lc_cont c) ->
     (lc_work (work_inftapp A1 A2 c))
 | lc_work_sub : forall (A1 A2:typ),
     (lc_typ A1) ->
     (lc_typ A2) ->
     (lc_work (work_sub A1 A2))
