      ( forall Y , Y \notin  L  -> ds_in X  ( open_dtyp_wrt_dtyp T (dtyp_var_f Y) )  )  ->
     ds_in X (dtyp_all T)
 | dsin_union : forall (X:typvar) (T1 T2:dtyp),
     ds_in X T1 ->
     ds_in X T2 ->
     ds_in X (dtyp_union T1 T2)
 | dsin_intersection : forall (X:typvar) (T1 T2:dtyp),
     ds_in X T1 ->
     ds_in X T2 ->
     ds_in X (dtyp_intersection T1 T2).

