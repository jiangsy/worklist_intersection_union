 | lc_dtyp_union : forall (T1 T2:dtyp),
     (lc_dtyp T1) ->
     (lc_dtyp T2) ->
     (lc_dtyp (dtyp_union T1 T2))
 | lc_dtyp_intersection : forall (T1 T2:dtyp),
     (lc_dtyp T1) ->
     (lc_dtyp T2) ->
     (lc_dtyp (dtyp_intersection T1 T2)).

