Inductive ds_in : typvar -> dtyp -> Prop :=    (* defn ds_in *)
 | dsin_var : forall (X:typvar),
     ds_in X (dtyp_var_f X)
 | dsin_arrow1 : forall (X:typvar) (T1 T2:dtyp),
     lc_dtyp T2 ->
     ds_in X T1 ->
     ds_in X (dtyp_arrow T1 T2)
 | dsin_arrow2 : forall (X:typvar) (T1 T2:dtyp),
     lc_dtyp T1 ->
     ds_in X T2 ->
     ds_in X (dtyp_arrow T1 T2)
